<p><!--:zh--><br />
<h2><strong>什么是Kyani</strong></h2>
<p><img title="阿拉斯加蓝莓的发现" rel="example3 " class="alignright example3 cboxElement" src="http://www.hykyani.com/hykyani.com/wp-content/uploads/2012/04/阿拉斯加蓝莓的发现.jpg" alt="阿拉斯加蓝莓的发现" width="260" height="312" /></p>
<p>公司的名称，Kyäni，是阿拉斯加当地特领吉族的土语，意思是<span class="zw_color">“强有力的天然药物”</span>。</p>
<p>Kyani公司的创办人之一迪克和妻子盖尔鲍威尔在阿拉斯加考察的时候，遇到了一位研究特领吉族部落的研究员，他告诉鲍威尔夫妇他对这个健康的部落十分地着迷，因为该族群中<span class="zw_color">“并没有记载心脏病、癌症、糖尿病以及其他许多我们常见的疾病”</span>。通过对他们生活习惯的观察发现，部落的人们经常食用野生蓝莓及野生三文鱼。</p>
<p>今天Kyani代表着健康及世界上拥有着健康生活方式的人。Kyani来到中国，入乡随俗，被称作“凯安尼”或“凯亚尼”。</p>
<div class="clear"></div>
<h2><strong>Kyani的历史</strong></h2>
<p>Kyani的创始人迪克鲍威尔夫妇(Dick&#038;Gayle Powell)了解到阿拉斯加野生蓝莓独一无二的功效后，卖掉了他们在美国创办并经营的一家全国性连锁公司。正如我们所有人一样，他们也看到了倍受国际媒体关注的有关蓝莓的健康功效的报导，这些报导几乎天天都有。然而，他们发现阿拉斯加野生蓝莓与普通蓝莓完全不同。很多世纪以来，阿拉斯加当地人一直将野生蓝莓用作有效的天然药物和天然的健康食品来源。阿拉斯加野生蓝莓所产生的抗氧化物超出普通蓝莓10倍。鲍威尔夫妇迅速取得了对生长在<span class="zw_color">阿拉斯加当地商用土地上的野生蓝莓的专有权</span>。他们的美好愿景是通过让人们服用从阿拉斯加野生蓝莓中特别提取的果汁，以帮助人们显著改善健康状况。</p>
<p>在阿拉斯加野生蓝莓果汁开发成功后，人们纷纷证明了该产品能够显著有效和快速地改善人体健康。他们认为自己的资金不足以满足该产品的开发等资金需求。因此，他们分享了他们的愿景给所认识的两个非常成功的家族，即汉森家族和泰勒家族。从鲍威尔夫妇了解到阿拉斯加野生蓝莓的功效，并亲自体验了阿拉斯加野生莓的神奇作用后，汉森家族和泰勒家族决定为这一事业提供资金支持，以帮助他们夫妇实现他们的美好愿景。汉森和泰勒的加入为Kyani注入了新的活力，后来他们在又出资购买了<span class="zw_color">南太平洋大溪地诺丽的70年的开采权</span>。用于生产诺丽精华——尼多乐(Kyani Nitro FX)。</p>
<p>至此，Kyani对产品原料享有的独有权，使得Kyani产品在市场上保持鲜明的特色和优越的竞争力。创办人的远见卓识造就了与众不同的Kyani，在稳固的财政基础之上，是Kyani产品的独特功效，改变着人们的生活，书写着Kyani的故事。</p>
