<p><img class="alignleft" title="kyani新舒康_心理健康" src="http://www.hykyani.com/hykyani.com/wp-content/uploads/2013/03/kyani-sunset_mental-health2.jpg" alt="kyani新舒康_心理健康" width="600" height="300" /></p>
<p>卡罗林斯卡医学院（瑞典斯德哥尔摩）的一项突破性研究使我们重新认识了维生素E对认知相关疾病的作用。维生素E的摄入不足会造成认知功能障碍，血浆中的生育酚及生育三烯酚含量过低很可能会引起轻度认知功能障碍（相关率92%）和老年痴呆症（相关率94%），现今有证据表明阿尔法生育酚对轻度认知功能障碍和老年痴呆症的作用是矛盾的，但生育三烯酚含量的摄取却显现出了很好的保护效果。</p>
<p>临床医学对轻度认知功能障碍和老年痴呆症病人血浆中的生育三烯酚和生育酚含量进行了测试，研究者发现维生素E含量的减少状况与病情有很大的关系。瑞典的人口研究显示生育三烯酚和生育酚水平的提高会减少老年痴呆症的发病几率高达50%。这项研究中阿尔法及贝塔生育酚和生育三烯酚起得作用最大。</p>
<p>在芬兰的一项研究中，伽马生育酚起到的保护作用最好，这项实验考查了全球的认知水平，大量的总生育三烯酚含量会大大的提高认知水平，弗朗西斯卡博士是首席研究员，她虽得到了生育酚和生育三烯酚对痴呆症保护具有重要作用这一结论，但是她还提出具体的治疗方法以及预防手段需要临床上基于每种维生素E形式进行分别的试验。</p>
<p>至今为止，阿尔法生育酚以外的生育酚及个别生育三烯酚对认知能力的作用还未在人口研究中明朗，至今的研究虽没有大量的证明，却是强烈建议作为参考的。弗朗西斯卡博士在进一步的区分性研究中指出，维生素E 有效成分的分离指日可待，那时，它们将是身体健康的保护伞。</p>
<p>生育三烯酚对心理健康好处的研究是神经生物学科的延伸，现在已经有动物实验证明生育三烯酚和阿尔法生育酚对心理创伤的效果，这种研究成果的临床实验正在规划中。</p>
<p>生育三烯酚还可以减轻由于糖尿病引起的神经损伤，对交感和副交感神经的失调有积极的作用，特别对家族性自主神经异常也有非常好的效果；患上这种遗传病的孩子常常会无泪，有严重的自理危机、极度的高血压，以及心脏频率失常，还伴随着持久的恶心症状。患上这种病的孩子在补充生育三烯酚后可以减少自理危机，可以自然流泪，这些都是自主神经功能的恢复。</p>
