<p><img class="aligncenter size-full wp-image-2193" title="kyani team" src="http://www.hykyani.com/hykyani.com/wp-content/uploads/2013/01/6.jpg" alt="" width="600" height="267" /></p>
<p>亲爱的Kyani家人和新朋友：</p>
<p>好久不见了，做为站长的我，必须要告诉大家为什么这么久都疏于网站更新，其实我有好多内容计划要更新，但是我一直在为大家做工具支持，现在我们有简约又全面的产品介绍及见证资料，有适用于大小会议的易拉宝，有最新的PPT资料，有最新翻译的视频。而且，昨天参加我们主办的300人健康养生会的朋友们是否也看到我们更加流畅的会议流程了呢？这些都是各位会员朋友帮助我一起实现的，在此谢谢大家！我准备在接下来的时间不断地完善网站，让大家有更好更全面的信息平台。大家有建议和要求可以发送邮件到hykyani@gmail.com或通过网站中的其他联系方式联系，谢谢！</p>
<p>今天我想说说一个健康的团队给我们消费者带来的利益。</p>
<h3>消费者会获得最专业的健康支持。</h3>
<p>我们的团队中有从事中医养生15年的领导人王秀丽，也有国家级营养师崇永华、王建华等，他们已经通过Kyani凯安尼帮助了几百消费者，给消费者最好的产品使用指导及问题解答，并不断地通过国际大会同Kyani医疗与科学委员会主席阿巴斯博士以及其他优秀的科学家交流，带给我们最新的资讯。比如，领导王秀丽在日本通过阿巴斯博士的演讲得知了阿巴斯博士对产品成分的进一步说明，以及对Kyani产品抗氧化能力进一步提升的计划，这些对于消费者来说都是非常有意义的，可以让我们使用产品的效果最大化，这意味着我们花最少的钱可以有最大的效果。</p>
<h3>消费者真正地会建立健康的生活方式</h3>
<p>相信参加过我们俱乐部活动的朋友基本都会感受到我们团队的健康氛围，在这里我们会认识真诚的朋友圈，通过彼此的交流与分享我们可以彼此鼓励，一起建立健康的生活方式，改变我们的生活。Kyani凯安尼给我们的并不仅是顶级的金三角组合，还有一个温暖的家。</p>
<h3>消费者能够越来越优惠地享受产品</h3>
<p>我们相信只要你是消费者，真正认真用过Kyani凯安尼的产品，一定会对Kyani凯安尼的产品有非常有力的见证。而我们的团队正是以产品为导向来做着这爱的事业，从而获得稳健的发展。当一个消费者因为一样产品而获得改变的时候，他一定会把这样的产品推荐给周围的朋友，作为爱心传递的同时，公司还会感激你带给它新的消费者并给予你丰富的回报，从而越来越优惠地享受产品，这是一个自然的过程。在这中间，团队会帮你告诉朋友到底这些产品是怎么起作用的，产品是否能帮助他们解决自身和家人的问题，如果成为会员，就能终身享受这样的服务，是不是很幸福呢？</p>
<h3>消费者也能享受和经营者一样的产品价格</h3>
<p>Kyani凯安尼公司创造性地实现了这一点，每个人只有成为会员才可以享受最纯正的产品及进一步的优惠，而每一位Kyani人使用产品全部是统一价，这个规则适用全世界。在整个团队里每一个人都不存在谁赚谁钱的关系，而是通过团队成员与自己的努力可以更多地为自己创造价值，进而帮助团队的进一步发展，你会渐渐成为经营者，同时你会发现，你在帮助更多的人改变健康，改变生活。</p>
<p>如果你选择成为消费者，一定要记住我的忠告：选择健康的、你觉得“合得来”的团队，因为她会真正地关心你，给你带来真正的快乐、健康与富足。</p>
<p>如果你已经是团队的一员，那么恭喜你已经找到了温暖的家，一定要配合大家一起营造更加温馨的氛围，无私地付出。还有最重要的，按时好好用产品，让爱你的大家听到你获得健康的见证！</p>
